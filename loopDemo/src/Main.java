public class Main {

    public static void main(String[] args) {
    // for
        for (int i = 1; i <= 10; i++) {

            System.out.println(i);


        }
        System.out.println("for döngüsüü bitti");
        //while
        int i = 1;
        while (i < 10) {
            i++;
            System.out.println(i);
        }
        System.out.println("While bitti");
    }

}
